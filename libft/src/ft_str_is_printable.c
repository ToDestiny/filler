#include "../inc/libft.h"

int	ft_str_is_printable(char *str)
{
	int i;
	int n;

	n = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 0 && str[i] <= 31) || str[i] == 127)
		{
			i++;
			n = 1;
		}
		else
			i++;
	}
	if (n == 1)
		return (0);
	else
		return (1);
}
