#include "../inc/libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int	i;

	i = 0;
	if (s1 && s2)
	{
		if (!n)
			return (1);
		while ((s1[i] == s2[i]) && s1[i] && s2[i] && n - 1)
		{
			++i;
			--n;
		}
		if (s1[i] == s2[i])
			return (1);
		return (0);
	}
	return (0);
}
